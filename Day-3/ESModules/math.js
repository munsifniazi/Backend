const add = (a, b) => {
  return a + b;
};

const subtract = (a, b) => {
  return a - b;
};

const multiply = (a, b) => {
  return a * b;
};

const divide = (a, b) => {
  return a / b;
};

const PI = 3.14;

// Exporting multiple functions as an object

export { add, subtract, multiply, divide, PI };